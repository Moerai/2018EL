package com.example.dudgn.androidchart;

public class Values {
    private int valueX;
    private int valueY;

    public Values(int valueX, int valueY) {
        this.valueX = valueX;
        this.valueY = valueY;
    }

    public int getValueX() {
        return valueX;
    }

    public void setValueX(int valueX) {
        this.valueX = valueX;
    }

    public int getValueY() {
        return valueY;
    }

    public void setValueY(int valueY) {
        this.valueY = valueY;
    }
}
